    @Override
    public PageUtils queryPageByCondition(Map<String, Object> params) {

        QueryWrapper<SpuInfoEntity> wrapper = new QueryWrapper<>();

        String key = (String) params.get("key");
        if (!StringUtils.isEmpty(key)) {
            wrapper.and((w) -> {
                w.eq("id", key).or().like("spu_name", key);
            });
        }
        // status=1 and (id=1 or spu_name like xxx)
        String status = (String) params.get("status");
        if (!StringUtils.isEmpty(status)) {
            wrapper.eq("publish_status", status);
        }

        String brandId = (String) params.get("brandId");
        if (!StringUtils.isEmpty(brandId) && !"0".equalsIgnoreCase(brandId)) {
            wrapper.eq("brand_id", brandId);
        }

        String catelogId = (String) params.get("catelogId");
        if (!StringUtils.isEmpty(catelogId) && !"0".equalsIgnoreCase(catelogId)) {
            wrapper.eq("catalog_id", catelogId);
        }

        /**
         * status: 2
         * key:
         * brandId: 9
         * catelogId: 225
         */

        IPage<SpuInfoEntity> page = this.page(
                new Query<SpuInfoEntity>().getPage(params),
                wrapper
        );

        return new PageUtils(page);
    }

    /**
     * 商品上架
     *
     * @param spuId
     */
    @Override
    public void up(Long spuId) {
        //根据spu的信息查询所有的sku的信息
        List<SkuInfoEntity> skuInfoEntityList = skuInfoDao.selectList(new QueryWrapper<SkuInfoEntity>().eq("spu_id", spuId));

        //根据spuI的查询出所有要检索的属性
        List<ProductAttrValueEntity> searchProductAttrValueEntityList = productAttrValueService.selectSearchAttrValues(spuId);

        //根据skuId查询出是否有库存
        List<Long> skuLists = skuInfoEntityList.stream().map(skuInfoEntity -> skuInfoEntity.getSkuId()).collect(Collectors.toList());
        R<List<SkuHasStock>> hasStockR = wareFeignService.hasStock(skuLists);
        List<SkuHasStock> hasStockRData = hasStockR.getData();
        Map<Long, Boolean> hasStockMap = hasStockRData.stream().collect(Collectors.toMap(SkuHasStock::getSkuId, item -> item.getHasStock()));
        //封装每个SkuEsModel信息
        List<SkuEsModel> esModels = skuInfoEntityList.stream().map(skuInfoEntity -> {
            SkuEsModel esModel = SkuEsModel.builder().build();
            BeanUtils.copyProperties(skuInfoEntity, esModel);
            esModel.setSkuPrice(skuInfoEntity.getPrice());
            esModel.setSkuImg(skuInfoEntity.getSkuDefaultImg());
            //是否有库存
            Boolean hasStock = hasStockMap.get(skuInfoEntity.getSkuId());
            esModel.setHasStock(hasStock);
            //热度评分
            esModel.setHotScore(0l);
            //查询品牌和分类的名字的信息
            BrandEntity brandEntity = brandDao.selectById(skuInfoEntity.getBrandId());
            if (brandEntity != null) {
                esModel.setBrandId(skuInfoEntity.getBrandId());
                esModel.setBrandImg(brandEntity.getLogo());
                esModel.setBrandName(brandEntity.getName());
            }
            //查询分类信息数据
            CategoryEntity categoryEntity = categoryDao.selectById(skuInfoEntity.getCatalogId());
            if (categoryEntity != null) {
                esModel.setCatalogId(skuInfoEntity.getCatalogId());
                esModel.setCatalogName(categoryEntity.getName());
            }
            //查询所有可以被用来检索的属性
            List<SkuEsModel.Attrs> attrs = new ArrayList<>();
            searchProductAttrValueEntityList.stream().forEach(productAttrValueEntity -> {
                SkuEsModel.Attrs attr = SkuEsModel.Attrs.builder()
                        .attrId(productAttrValueEntity.getAttrId())
                        .attrName(productAttrValueEntity.getAttrName())
                        .attrValue(productAttrValueEntity.getAttrValue())
                        .build();
                attrs.add(attr);
            });
            esModel.setAttrs(attrs);
            return esModel;
        }).collect(Collectors.toList());
        //调用es服务保存数据
        R r = searchFeignService.productStateUp(esModels);
    }

